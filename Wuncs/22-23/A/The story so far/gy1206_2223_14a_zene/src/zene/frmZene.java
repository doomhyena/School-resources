    private void lejatszas() {
        if (c!=null)
            c.close();
        
        
        File f = files[lbSzamok.getSelectedIndex()];
        AudioInputStream ais;
        try{
            ais = AudioSystem.getAudioInputStream(f);
            c = AudioSystem.getClip();
            c.open(ais);
            c.start();
        }catch (Exception e){
            System.out.println("Hiba: "+e);
        }
    }
