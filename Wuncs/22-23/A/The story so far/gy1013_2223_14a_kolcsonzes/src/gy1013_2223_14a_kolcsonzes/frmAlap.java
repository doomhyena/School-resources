    static boolean f_tv = false;
    static boolean f_cv = false;
    private void lbAdatokMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbAdatokMouseClicked
        if (evt.getClickCount() == 2){
            if (rbSzabad.isSelected()){
                
            if (rbTulaj.isSelected()){
                t_index = lbAdatok.getSelectedIndex();
                lblTulaj1.setText("Örökbefogadó neve: "+
             frm_Bej.szabad_tulajok.get(
             lbAdatok.getSelectedIndex()).getNev());
                befogado = frm_Bej.szabad_tulajok.get(
                        lbAdatok.getSelectedIndex());
                tv = true;
            }
            else{
                c_index = lbAdatok.getSelectedIndex();
                lblCica.setText("Örökbefogadott neve: "+
             frm_Bej.szabad_cicak.get(
             lbAdatok.getSelectedIndex()).getNev());
                befogadott =frm_Bej.szabad_cicak.get(
                        lbAdatok.getSelectedIndex());
                cv = true;
            }
            if (tv && cv){
                btnBefogad.setText("Befogad");
                btnBefogad.setVisible(true);
            }
            }
            else if (rbFoglalt.isSelected()){
                
                if (rbTulaj.isSelected()){
                    t_index = lbAdatok.getSelectedIndex();
                    lblTulaj1.setText("Örökbefogadó neve: "+
             frm_Bej.foglalt_tulajok.get(
             lbAdatok.getSelectedIndex()).getNev());
                befogado = frm_Bej.foglalt_tulajok.get(
                        lbAdatok.getSelectedIndex());
                f_tv = true;
                }
                else if (rbCicak.isSelected()){
                    c_index = lbAdatok.getSelectedIndex();
                    lblCica.setText("Örökbefogadott neve: "+
             frm_Bej.foglalt_cicak.get(
             lbAdatok.getSelectedIndex()).getNev());
                befogadott = frm_Bej.foglalt_cicak.get(
                        lbAdatok.getSelectedIndex());
                f_cv = true;
                }
                if (f_tv && f_cv){
                    btnBefogad.setText("Felold");
                    btnBefogad.setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_lbAdatokMouseClicked

    private void btnBefogadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBefogadActionPerformed
        if (btnBefogad.getText().equals("Befogad")){
        Kolcsonzes k = new Kolcsonzes(befogado, befogadott);
        kiir(k);
        frm_Bej.szabad_cicak.get(c_index).setFoglalt("Igen");
        frissit(frm_Bej.path_c);
        frm_Bej.szabad_cicak.removeElement(befogadott);
        frm_Bej.foglalt_cicak.addElement(befogadott);
        
        frm_Bej.szabad_tulajok.get(t_index).setTul("Igen");
        frissit_tulaj(frm_Bej.path_t);
        frm_Bej.szabad_tulajok.removeElement(befogado);
        frm_Bej.foglalt_tulajok.addElement(befogado);
        
        btnBefogad.setVisible(false);
        cv = false;
        tv = false;
        }
        else if (btnBefogad.getText().equals("Felold")){
        Kolcsonzes k = new Kolcsonzes(befogado, befogadott);
        frm_Bej.foglalt_cicak.get(c_index).setFoglalt("Nem");
        frm_Bej.foglalt_cicak.removeElement(befogadott);
        frm_Bej.szabad_cicak.addElement(befogadott);
        frissit(frm_Bej.path_c);
        
        frm_Bej.foglalt_tulajok.get(t_index).setTul("Nem");
        frm_Bej.foglalt_tulajok.removeElement(befogado);
        frm_Bej.szabad_tulajok.addElement(befogado);
        frissit_tulaj(frm_Bej.path_t);
        
        btnBefogad.setVisible(false);
        f_cv = false;
        f_tv = false;  
        }
        
    }//GEN-LAST:event_btnBefogadActionPerformed

