    private void beolvas(Scanner be, String s) {
        be.nextLine();
        String sor;
        String [] adatok;
        if (s.equals("Tulajok.csv")){
            path_t = f.getPath();
            while (be.hasNextLine()){
                sor = be.nextLine();
                adatok = sor.split(";");
                Tulaj t = new Tulaj(
                adatok[0],
                Integer.parseInt(adatok[1]),
                adatok[2]);
                if (t.getTul().equals("Igen"))
                    foglalt_tulajok.addElement(t);
                else
                    szabad_tulajok.addElement(t);
                
            }
        }
        else{
            path_c = f.getPath();
            while (be.hasNextLine()){
                sor = be.nextLine();
                adatok = sor.split(";");
                Cica c = new Cica(
                adatok[0],
                Integer.parseInt(adatok[1]),
                adatok[2],adatok[3]);
                if (c.getFoglalt().equals("Nem"))
                    szabad_cicak.addElement(c);
                else
                    foglalt_cicak.addElement(c);
                
            }
        }
    }
