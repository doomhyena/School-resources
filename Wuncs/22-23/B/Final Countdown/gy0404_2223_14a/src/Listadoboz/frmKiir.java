    private void kiir(String name) {
        try {
            FileWriter fw = new FileWriter(name);
            for (int i = 0; i < frmListak.szurt.size(); i++) {
                fw.write(frmListak.szurt.get(i)+"\n");
            }
            fw.close();
            JOptionPane.showMessageDialog(null,"Sikeres kiírás!");
        } catch (IOException ex) {
            System.out.println("Hiba: "+ex);
        }
    }
