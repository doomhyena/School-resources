    private void kiir(Tars t) {
        try {
            FileWriter fw =
               new FileWriter("Befogadasok.csv",true);
            fw.write(t+"\n");
            fw.close();
        } catch (IOException ex) {
            System.out.println(ex);
        }
    }

    private void frissit(String path_c) {
        try {
            FileWriter fw = new FileWriter(path_c);
            fw.write("Nev;Kor(honap);Kep;Foglalt\n");
            for (int i = 0; i < frmBej.szabad_cicak.getSize(); i++) {
                fw.write(frmBej.szabad_cicak.get(i).getNev()+";"+
                        frmBej.szabad_cicak.get(i).getKor()+";"+
               frmBej.szabad_cicak.get(i).getKep().getDescription()+";"+
                        frmBej.szabad_cicak.get(i).getFoglalt()+"\n");
            }
            for (int i = 0; i < frmBej.foglalt_cicak.getSize(); i++) {
                fw.write(frmBej.foglalt_cicak.get(i).getNev()+";"+
                        frmBej.foglalt_cicak.get(i).getKor()+";"+
               frmBej.foglalt_cicak.get(i).getKep().getDescription()+";"+
                        frmBej.foglalt_cicak.get(i).getFoglalt()+"\n");
            }
            fw.close();
        } catch (IOException ex) {
            System.out.println(ex);
        }
    }

    private void frissit_tulaj(String path_t) {
        try {
            FileWriter fw = new FileWriter(path_t);
            fw.write("Nev;Kor;Tulajdonos\n");
            for (int i = 0; i < frmBej.szabad_tulajok.getSize(); i++) {
                fw.write(frmBej.szabad_tulajok.get(i).getNev()+";"+
                        frmBej.szabad_tulajok.get(i).getKor()+";"+
               frmBej.szabad_tulajok.get(i).getTul()+"\n");
            }
            for (int i = 0; i < frmBej.foglalt_tulajok.getSize(); i++) {
                fw.write(frmBej.foglalt_tulajok.get(i).getNev()+";"+
                        frmBej.foglalt_tulajok.get(i).getKor()+";"+
               frmBej.foglalt_tulajok.get(i).getTul()+"\n");
            }
            fw.close();
        } catch (IOException ex) {
            System.out.println(ex);
        }
    }
