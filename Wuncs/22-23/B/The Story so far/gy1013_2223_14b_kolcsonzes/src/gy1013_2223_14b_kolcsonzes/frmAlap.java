    static Tulaj ta;
    static Cica ca;
    static boolean tv = false;
    static boolean cv = false;
    static int t_index;
    static int c_index;
    static boolean f_tv = false;
    static boolean f_cv = false;
    private void lbAdatokMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbAdatokMouseClicked
        if (evt.getClickCount() == 2){
            if (rbSzabad.isSelected()){
            if (rbTulajok.isSelected()){
                t_index = lbAdatok.getSelectedIndex();
                lblTulaj.setText("Örökbefogadó neve: "+
                frmBej.szabad_tulajok.get(
                lbAdatok.getSelectedIndex()).getNev());
                ta = frmBej.szabad_tulajok.get(
                lbAdatok.getSelectedIndex());
                tv = true;
            }
            else{
                c_index = lbAdatok.getSelectedIndex();
                lblCica.setText("Cica neve: "+
                frmBej.szabad_cicak.get(
                lbAdatok.getSelectedIndex()).getNev());
                ca = frmBej.szabad_cicak.get(
                lbAdatok.getSelectedIndex());
                cv = true;
            }
            if (tv && cv){
                btnBefogad.setText("Befogad");
                btnBefogad.setVisible(true);
            }
            }
            else if (rbFoglalt.isSelected()){
                if (rbTulajok.isSelected()){
                t_index = lbAdatok.getSelectedIndex();
                lblTulaj.setText("Örökbefogadó neve: "+
                frmBej.foglalt_tulajok.get(
                lbAdatok.getSelectedIndex()).getNev());
                ta = frmBej.foglalt_tulajok.get(
                lbAdatok.getSelectedIndex());
                f_tv = true;
            }
            else{
                c_index = lbAdatok.getSelectedIndex();
                lblCica.setText("Cica neve: "+
                frmBej.foglalt_cicak.get(
                lbAdatok.getSelectedIndex()).getNev());
                ca = frmBej.foglalt_cicak.get(
                lbAdatok.getSelectedIndex());
                f_cv = true;
            }
            if (f_tv && f_cv){
                btnBefogad.setText("Felold");
                btnBefogad.setVisible(true);
            }
                
            }
        }
    }//GEN-LAST:event_lbAdatokMouseClicked

    private void btnBefogadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBefogadActionPerformed
        if (btnBefogad.getText().equals("Befogad")){
        Tars t = new Tars(ta, ca);
        kiir(t);
        frmBej.szabad_cicak.get(c_index).setFoglalt("Igen");
        frissit(frmBej.path_c);
        frmBej.szabad_cicak.removeElement(ca);
        frmBej.foglalt_cicak.addElement(ca);
        
        frmBej.szabad_tulajok.get(t_index).setTul("Igen");
        frissit_tulaj(frmBej.path_t);
        frmBej.szabad_tulajok.removeElement(ta);
        frmBej.foglalt_tulajok.addElement(ta);
        
        btnBefogad.setVisible(false);
        cv = false;
        tv = false;
        }
        else if (btnBefogad.getText().equals("Felold")){
        Tars t = new Tars(ta, ca);
        
        frmBej.foglalt_cicak.get(c_index).setFoglalt("Nem");
        frissit(frmBej.path_c);
        frmBej.foglalt_cicak.removeElement(ca);
        frmBej.szabad_cicak.addElement(ca);
        
        frmBej.foglalt_tulajok.get(t_index).setTul("Nem");
        frissit_tulaj(frmBej.path_t);
        frmBej.foglalt_tulajok.removeElement(ta);
        frmBej.szabad_tulajok.addElement(ta);
        
        btnBefogad.setVisible(false);
        f_cv = false;
        f_tv = false; 
        }
    }//GEN-LAST:event_btnBefogadActionPerformed

