    private void beolvas() throws FileNotFoundException, ParseException {
        File f = new File("futok.txt");
        SimpleDateFormat sdf1 = 
                new SimpleDateFormat("yyyy.MM.dd");
        SimpleDateFormat sdf2 =
                new SimpleDateFormat("mm:ss.SS");
        Scanner be = new Scanner(f,"ISO-8859-2");
        String sor;
        String [] adatok;
        while(be.hasNextLine()){
            sor = be.nextLine();
            adatok = sor.split(";");
            futok.add(new Futo(adatok[0], adatok[1],
                    sdf1.parse(adatok[2]), adatok[3],
                    sdf2.parse(adatok[4])));
            nevek.addElement(adatok[1]);
        }
    }
