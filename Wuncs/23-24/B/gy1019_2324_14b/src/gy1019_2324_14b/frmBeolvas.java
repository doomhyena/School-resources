    ArrayList<Fiu> fiuk =
            new ArrayList<>();
    private void feltolt() {
        File f = new File("fiuk.csv");
        try {
            Scanner be = new Scanner(f,"ISO-8859-2");
            be.nextLine();
            String sor;
            String [] adatok;
            while(be.hasNextLine()){
                sor = be.nextLine();
                adatok = sor.split(";");
                fiuk.add(
                   new Fiu(adatok[0],adatok[1]));
            }
        } catch (Exception e) {
            System.out.println("Hiba: "+e);
        }
    }
