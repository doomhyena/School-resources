    private void feltolt() {
        File f = new File("diakok.csv");
        try {
            Scanner be = new Scanner(f,"UTF-8");
            be.nextLine();
            String sor;
            String adatok [];
            while(be.hasNextLine()){
                sor = be.nextLine();
                adatok = sor.split(";");
                Diak d = new Diak(adatok[0],
                Integer.parseInt(adatok[1]), adatok[2]);
                diakok.addElement(d);
                if (adatok[2].equals("Férfi"))
                    fiuk.addElement(d);
                else
                    lanyok.addElement(d);
            }
            
        } catch (Exception e) {
        }
    }

    private double atlag(DefaultListModel<Diak> lista) {
        double avg = 0;
        for (int i = 0; i < lista.size(); i++) {
            avg += lista.get(i).getKor();
        }
        
        return avg/lista.size();
    }
