    private void btnSzamolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSzamolActionPerformed
        a = tfA.getText();
        b = tfB.getText();
        c = tfC.getText();
        if (a.equals("") || b.equals("") || c.equals(""))
            JOptionPane.showMessageDialog(
                    this,"Minden mezőt töltsön ki");
        else if (a.equals("0"))
            JOptionPane.showMessageDialog(
                    this,"'a' értéke nem lehet 0");
        else{
            e = new Egyenlet(Double.parseDouble(a),
                    Double.parseDouble(b),
                    Double.parseDouble(c));
            tfMo.setText(e.megoldas());
        }
    }//GEN-LAST:event_btnSzamolActionPerformed

    private void btnTarolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTarolActionPerformed
        if (tfMo.getText().equals(""))
            JOptionPane.showMessageDialog(
            this,"Nincs mit tárolni!");
        else{
            mok.addElement(tfMo.getText());
            tfMo.setText("");
            
        }
    }//GEN-LAST:event_btnTarolActionPerformed

