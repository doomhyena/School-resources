    private void feltolt() {
        File f = new File("filmek.csv");
        try {
            Scanner be = new Scanner(f,"UTF-8");
            String sor;
            String adatok[];
            be.nextLine();
            while(be.hasNextLine()){
                sor = be.nextLine();
                adatok = sor.split(";");
                filmek.addElement(
                new Film(adatok[0],
                 Integer.parseInt(adatok[1]),
                Integer.parseInt(adatok[2])));
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }
