    private void beolvas(File f) {
        try {
            Scanner be = new Scanner(f,"ISO-8859-2");
            while (be.hasNextLine()){
                orszagok.addElement(new Orszag (
                     be.nextLine(),
                     Integer.parseInt(be.nextLine()),
                     be.nextLine()));
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    private String szamol() {
        int t = 0;
        int k = 0;
        
        for (int i = 0; i < orszagok.size(); i++) {
            if (orszagok.get(i).nepe > 10000)
                t++;
            else
                k++;
        }
        if (rbTobb.isSelected())
            return t+"";
        else
            return k+"";
    }

    private String minmax(int index) {
        Orszag min = orszagok.get(0);
        Orszag max = orszagok.get(0);
        
        for (int i = 0; i < orszagok.size(); i++) {
            if (orszagok.get(i).nepe < min.nepe)
                min = orszagok.get(i);
            if (orszagok.get(i).nepe > max.nepe)
                max = orszagok.get(i);
        }
        
        if (index == 0)
            return min.nev;
        else
            return max.nev;
        
    }

    private String atl() {
        double avg = 0;
        for (int i = 0; i < orszagok.size(); i++) {
            avg += orszagok.get(i).nepe;
        }
        avg /= orszagok.size();
        return String.format("Įtlag: %.2fe fõ.", avg);
    }

    private void eu() {
        for (int i = 0; i < orszagok.size(); i++) {
            if (orszagok.get(i).kont.equals("Európa"))
                euk.addElement(orszagok.get(i));
        }
    }
