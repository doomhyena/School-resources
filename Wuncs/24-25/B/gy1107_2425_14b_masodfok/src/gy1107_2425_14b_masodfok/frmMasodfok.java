    private void masodfok() {
        try{
            double a = Double.parseDouble(tfA.getText());
            if (a!=0){
                double b = Double.parseDouble(tfB.getText());
                double c = Double.parseDouble(tfC.getText());
                double diszkr = b*b-4*a*c;
                if (diszkr > 0){
                    double x1 = (-b+Math.sqrt(diszkr))/(2*a);
                    double x2 = (-b-Math.sqrt(diszkr))/(2*a);
                    System.out.printf("x1: %.2f, x2: %.2f ",x1,x2);
                    lblMegold.setText(String.format("x1: %.2f, x2: %.2f ",x1,x2));
                }
                else if (diszkr == 0){
                    double x = -b/(2*a);
                    System.out.println("x: "+x);
                    lblMegold.setText(String.format("x: %.2f",x));
                }
                else{
                    System.out.println("Nincs valós megoldás!");
                    lblMegold.setText("Nincs valós megoldás");
                }
            }
            else{
                JOptionPane.showMessageDialog(this,"'A' értéke nem lehet 0!");
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(this,"A mezőkbe CSAK valós SZÁMOK kerülhetnek!");
        }
    }
