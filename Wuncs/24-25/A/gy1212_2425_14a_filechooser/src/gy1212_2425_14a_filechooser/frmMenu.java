    private void feltolt(String path) {
        File f = new File(path);
        try {
            Scanner be = new Scanner(f,"UTF-8");
            String adatok [];
            String sor;
            while (be.hasNextLine()) {
                
                sor = be.nextLine();
                adatok = sor.split(";");
                System.out.println(Arrays.toString(adatok));
                
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
