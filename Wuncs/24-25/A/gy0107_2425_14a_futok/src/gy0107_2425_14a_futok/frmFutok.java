    private void feltolt(File f) {
        try {
            Scanner be = new Scanner(f,"ISO-8859-2");
            String sor;
            String [] adatok;
            while(be.hasNextLine()){
                sor = be.nextLine();
                adatok = sor.split(";");
                futok.addElement(new Futo(adatok[0], adatok[1], adatok[2],
                        adatok[3], adatok[4]));
                
                fut.add(new Futo(adatok[0], adatok[1], adatok[2],
                        adatok[3], adatok[4]));
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
