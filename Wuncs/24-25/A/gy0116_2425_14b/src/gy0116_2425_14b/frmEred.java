    private void rendez(DefaultListModel<Futo> futok, ArrayList<Futo> fut) {
        futok.clear();
        for (Futo n : fut) {
            futok.addElement(n);
        }
        
    }

    private void kiir() {
        try {
            FileWriter fw = new FileWriter("eredmenyek.txt");
            for (Futo n : frmFutok.fut) {
                fw.write(n+", "+n.getIdo()+"\n");                
            }
            fw.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }
