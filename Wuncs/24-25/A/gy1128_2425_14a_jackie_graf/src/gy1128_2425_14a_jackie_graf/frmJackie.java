    private void btnF4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnF4ActionPerformed
        max = eredmenyek.get(0);
        for (int i = 0; i < eredmenyek.getSize(); i++) {
            if (eredmenyek.get(i).getRaces() > max.getRaces())
                max = eredmenyek.get(i);
        }
        lblF4.setText("4. Feladat: "+max.getYear());
        
    }//GEN-LAST:event_btnF4ActionPerformed
    int hat;
    int het;
    private void btnF5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnF5ActionPerformed
        hat = 0;
        het = 0;
        
        for (int i = 0; i < eredmenyek.getSize(); i++) {
            if (eredmenyek.get(i).getYear() < 1970)
                hat += eredmenyek.get(i).getWins();
            else
                het += eredmenyek.get(i).getWins();
        }
        lblF5.setText(String.format("<html> 60-as évek: %d <br> 70-es évek: %d",hat,het));
        
    }//GEN-LAST:event_btnF5ActionPerformed

