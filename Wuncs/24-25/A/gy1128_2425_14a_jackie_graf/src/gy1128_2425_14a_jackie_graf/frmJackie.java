    private void feltolt() {
        File f = new File("jackie.txt");
        try {
            Scanner be = new Scanner(f,"UTF-8");
            be.nextLine();
            String [] adatok;
            String sor;
                                   
            while(be.hasNextLine()){
                sor = be.nextLine();
                adatok = sor.split("\t");
                eredmenyek.addElement(new Jackie(
                        Integer.parseInt(adatok[0]),
                        Integer.parseInt(adatok[1]),
                        Integer.parseInt(adatok[2]),
                        Integer.parseInt(adatok[3]),
                        Integer.parseInt(adatok[4]),
                        Integer.parseInt(adatok[5])));
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
