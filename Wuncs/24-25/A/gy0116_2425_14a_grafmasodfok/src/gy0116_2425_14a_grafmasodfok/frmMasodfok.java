    private boolean kitoltve() {
        for (JTextField n : tf) {
            if (n.getText().equals(""))
                return true;
        }
        return false;
    }

    private Egyenlet kreal() {
        return new Egyenlet(Double.parseDouble(tf.get(0).getText()),
                Double.parseDouble(tf.get(1).getText()),
                Double.parseDouble(tf.get(2).getText()));
    }

    private void feltolt() {
        File f = new File("egyenletek.txt");
        try {
            Scanner be = new Scanner(f,"UTF-8");
            String sor;
            while (be.hasNextLine()) {
                sor = be.nextLine();
                mok.addElement(sor);
                
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
