    private void feltolt() {
        File f = new File("nevek.csv");
        try {
            Scanner be = new Scanner(f,"UTF-8");
            String sor;
            String [] adatok;
            be.nextLine();
            
            while(be.hasNextLine()){
                sor = be.nextLine();
                adatok = sor.split(";");
                if (adatok[2].equals("0"))
                    no.addElement(new Szemely(adatok[0],
                    Integer.parseInt(adatok[1]),
                    Integer.parseInt(adatok[2])));
                else
                    ffi.addElement(new Szemely(adatok[0],
                    Integer.parseInt(adatok[1]),
                    Integer.parseInt(adatok[2])));
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    private void avg(DefaultListModel<Szemely> lista) {
        double a = 0;
        for (int i = 0; i < lista.size(); i++) {
            a += lista.get(i).getKor();
        }
        a /= lista.size();
        lblAvg.setText(String.format("┴tlag: %.2f ķv", a));
    }
